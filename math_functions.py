def add_numbers(a, b):
    return a + b

def subtract_numbers(a, b):
    return a - b

def multiply_numbers(a, b):
    return a * b

def divide_numbers(a, b):
    return a / b

def power_of_two(a):
    return a * a

def power_of_three(a):
    return a * a * a

if __name__ == "__main__":
    print("Adding:", add_numbers(2,4))
    print("Subtracting:", subtract_numbers(9,2))
    print("Multiplying: ", multiply_numbers(2, 4))
    print("Dividing: ", divide_numbers(2, 4))
    print("Power of two: ", power_of_two(3))
    print("Power of three: ", power_of_three(3))

